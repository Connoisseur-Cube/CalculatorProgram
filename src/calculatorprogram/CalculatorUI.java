    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        dispText = jTextPane1.getText();
        try{
        double answer = Calculation.evaluate(dispText); 
        int check = (int) answer;
        if(check == answer){
            dispText = String.valueOf(check);
        } else
        {
            dispText = String.valueOf(answer);
        }
        }
        catch(Exception e){
            ItsON obj = new ItsON();
            obj.setLabel("Syntax Error or Arithemtic Error");
            obj.setVisible(true);
        }
        answered = true;
        jTextPane1.setText(dispText);
        
    }//GEN-LAST:event_jButton6ActionPerformed

